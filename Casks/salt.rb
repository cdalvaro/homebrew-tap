  on_macos do
    sha256 arm:   "b9327739cc8a38139186d1615c4ce8a62075823e28bfb4cd097cd9f96ce7e00e",
           intel: "7ea0efef5b2c8283ea6e97eff4b1eba0d0c8bf518ca749893bccfe4c803090ac"
  end

